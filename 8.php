<b> 8. Дан массив с элементами 1, 2, 3, 4, 5, 6, 7, 8, 9. 
С помощью цикла foreach создайте строку '-1-2-3-4-5-6-7-8-9-'. </b>
</br> </br> </br>
<?php
	$tring = '';
	$arr = [1, 2, 3, 4, 5, 6, 7, 8, 9];
		foreach ($arr as $temp)
		{
				$tring .= '-'.$temp; 
				//$tring = $tring.'-'.$temp; 
		}		
		echo '\''.$tring.'-'.'\''.'.';

?>